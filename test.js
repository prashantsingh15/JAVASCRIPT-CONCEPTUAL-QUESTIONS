function checkGrade(p) {
    if(p>=60) {
        return true;
    }
    else {
        return false;
    }
    
}

console.log(checkGrade(70))


// The output is "true" because the function is designed to return true when the input value (p) is greater than or equal to 60, and in this case, the input value is 70, satisfying the condition.